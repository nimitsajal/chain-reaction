public class Move {
    private int rowPos;
    private int collPos;

    public int getRowPos() {
        return rowPos;
    }

    public void setRowPos(int rowPos) {
        this.rowPos = rowPos;
    }

    public int getCollPos() {
        return collPos;
    }

    public void setCollPos(int collPos) {
        this.collPos = collPos;
    }
}
